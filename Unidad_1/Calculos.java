
public class Calculos {

	public static void main(String[] args) {
		int a= 4;
		int b= 5;
		System.out.println(a+b);
		
		System.out.println(a-b);
		
		System.out.println(a*b);
		
		System.out.println(a/b);
		
		System.out.println (a%b);
		
		System.out.println(a>b);
		
		System.out.println(b++);
		//ejecuta línea y luego el número que sobra 
		
		System.out.println(++b);
		//ha somado dos veces, porque ya habia uno del b++
	
	}

}
